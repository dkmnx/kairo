func TestGenerateWindowsScript_WithEnvVars(t *testing.T) {
	testCases := []struct {
		name     string
		envVar   string
		expected string
	}{
		{"custom env var", "MY_API_KEY", "$env:MY_API_KEY"},
		{"anthropic default", "ANTHROPIC_AUTH_TOKEN", "$env:ANTHROPIC_AUTH_TOKEN"},
		{"openai style", "OPENAI_API_KEY", "$env:OPENAI_API_KEY"},
	}

	for _, tc := range testCases {
		t.Run(tc.name, func(t *testing.T) {
			cfg := ScriptConfig{
				AuthDir:    t.TempDir(),
				TokenPath:  `C:\temp\token`,
				CliPath:    `C:\claude.exe`,
				CliArgs:    []string{"--help"},
				EnvVarName: tc.envVar,
			}

			script := GenerateWindowsScript(tc.envVar, cfg)

			if !strings.Contains(script, tc.expected) {
				t.Errorf("Script should set %s, got: %s", tc.expected, script)
			}
		})
	}
}

