func TestGenerateWindowsScript_BasicOutput(t *testing.T) {
	cfg := ScriptConfig{
		AuthDir:    t.TempDir(),
		TokenPath:  `C:\temp\token`,
		CliPath:    `C:\Program Files\Claude\claude.exe`,
		CliArgs:    []string{"--help"},
		EnvVarName: "ANTHROPIC_AUTH_TOKEN",
	}

	script := GenerateWindowsScript("ANTHROPIC_AUTH_TOKEN", cfg)

	if !strings.Contains(script, "# Generated by kairo") {
		t.Error("Script should contain header comment")
	}
	if !strings.Contains(script, "$env:ANTHROPIC_AUTH_TOKEN") {
		t.Error("Script should set environment variable")
	}
	if !strings.Contains(script, "Get-Content") {
		t.Error("Script should use Get-Content to read token")
	}
	if !strings.Contains(script, "Remove-Item") {
		t.Error("Script should remove token file")
	}
	if !strings.Contains(script, "C:\\\\temp\\\\token") {
		t.Error("Script should contain token path (with escaped backslashes)")
	}
}

func TestGenerateWindowsScript_WithSpecialCharacters(t *testing.T) {
	cfg := ScriptConfig{
		AuthDir:    t.TempDir(),
		TokenPath:  `C:\temp\token`,
		CliPath:    `C:\Program Files\Claude\claude.exe`,
		CliArgs:    []string{"--prompt", "Hello $HOME and \"quotes\"", "--model", "sonnet-4"},
		EnvVarName: "ANTHROPIC_AUTH_TOKEN",
	}

	script := GenerateWindowsScript("ANTHROPIC_AUTH_TOKEN", cfg)

	if !strings.Contains(script, "`$HOME") {
		t.Error("Script should escape dollar sign in $HOME")
	}
	if strings.Contains(script, "$HOME") && !strings.Contains(script, "`$HOME") {
		t.Error("Dollar sign should be escaped")
	}
	if !strings.Contains(script, `\"quotes\"`) {
		t.Error("Script should escape double quotes properly")
	}
}

